#Estructura Condicional IF

#En el caso de que una condición se cumpla, ejecutaremos una o varias sentencias (líneas de código) y seguiremos con la ejecución del programa. 
#En el caso de que la condición no se cumpla no ejecutaremos dichas sentencias y solo continuaremos con la ejecución de nuestro programa.

#En un lenguaje de programación, decir que una condición se cumple para ejecutar determinadas sentencias, 
#significa que esa condición es verdadera o en terminos del lenguaje Python , significa que es True.


if True:
  print("La condición se cumplió")
  
#python utiliza una sangría o indentación, luego del if dejas un espacio en blanco en la linea de abajo
